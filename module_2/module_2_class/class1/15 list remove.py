# remove function will remove the item from the list
# it will not take any index number as an arguments
# it will take the item name as an argument

bazar_list = [
'lebu',
'chini',
'ada'
]

print('bazar_list: ', bazar_list)

bazar_list.remove('chini')
print('bazar_list after using remove function: ', bazar_list)